  		<div class="row">
  			<div class="col-md-6">
    			<h3><span>What</span> is Hackomotive?</h3>
    			<p>Hackomotive is a three-day competition between innovative teams who are on the path to create a significantly better car shopping experience. In an inspiring setting, with guidance from motivational speakers and industry experts, the participants will further develop their ideas into prototypes for products and services that could make a real difference for car shoppers. On the last afternoon, the teams will present their prototypes to the event’s judges in a "Shark Tank"-style format, competing for three cash prizes. At least one winning entrant will be eligible to have its idea accelerated by Edmunds.com.</p>
				<h3><span>When</span> will the event take place?</h3>
				<p>Tuesday, February 25th through Thursday, February 27th, 2014</p>
				<h3><span>Where</span> will the event take place?</h3>
				<p>Edmunds.com’s headquarters in sunny Santa Monica, California - the heart of “Silicon Beach”.</p>
				<h3><span>Why</span> would I want to participate in this event?</h3>
				<p>This special event brings innovators and disruptors from all over the country to present their creative solutions to a persistent problem. As a participant in this event, you will improve the car shopping experience for consumers around the country, network with impressively creative people and business leaders, have a memorable personal and professional development opportunity - and perhaps win fame and fortune in the process.</p>
			</div>
  			<div class="col-md-6">
				<iframe width="560" height="315" src="//www.youtube.com/embed/89mAsWOGxFk?rel=0" frameborder="0" allowfullscreen></iframe>
				
				<h3><span>Who</span> is eligible to participate?</h3>
				<p>Anyone with a great idea for improving the car shopping experience is eligible to apply to participate in Hackomotive. Each participant may be either:</p>
				<p>
					<ul>
						<li>a company or other entity, represented at Hackomotive by its designated representatives</li>
						<li>a team of individuals who come together to participate at Hackomotive</li>
						<li>an individual participant</li>
					</ul>
				</p>
